/// URL safe symbols.
///
/// An array of characters which can be safely used in urls.
/// Alphabet default for pillid. Is alphabet by default for pillid
///
/// # Example
///
/// ```
/// let id = pillid::pillid!(10, &pillid::alphabet::URLSAFE);
/// ```
